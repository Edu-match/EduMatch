/**
 * 編集ページでDBに保存された本文が空の場合に表示するデフォルトコンテンツ。
 * 表示ページ（/terms, /privacy）で表示されているデフォルト内容と一致させる。
 */
export function getDefaultContentForEdit(
  key: SitePageKey,
  body: string | null
): string {
  if (body?.trim()) return body;
  if (key === "terms") return DEFAULT_TERMS_MARKDOWN;
  if (key === "privacy") return DEFAULT_PRIVACY_MARKDOWN;
  if (key === "faq") return DEFAULT_FAQ_JSON;
  if (key === "about") return DEFAULT_ABOUT_MARKDOWN;
  if (key === "company_info") return DEFAULT_COMPANY_INFO_MARKDOWN;
  return "";
}

