/**
 * 教育公的文書のシードデータを knowledge_chunks / knowledge_documents に投入するスクリプト
 * 実行: npx tsx scripts/seed-knowledge.ts
 * Prisma (.env の DIRECT_URL) + OpenAI で直接DB接続。
 * knowledge_chunks の embedding は Prisma Unsupported のため executeRawUnsafe で挿入。
 */
import OpenAI from "openai";
import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();
const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY! });

// ─── テキストチャンク分割 ────────────────────────────────────────────────────

function splitIntoChunks(text: string, chunkChars = 2000, overlapChars = 200): string[] {
  const paragraphs = text.split(/\n{2,}/).map((p) => p.trim()).filter((p) => p.length > 0);
  const chunks: string[] = [];
  let current = "";

  for (const para of paragraphs) {
    if ((current + "\n\n" + para).length > chunkChars && current) {
      chunks.push(current.trim());
      current = current.slice(-overlapChars) + "\n\n" + para;
    } else {
      current = current ? current + "\n\n" + para : para;
    }
  }
  if (current.trim()) chunks.push(current.trim());
  return chunks.filter((c) => c.length > 30);
}

// ─── Embedding 生成 ──────────────────────────────────────────────────────────

async function embedTexts(texts: string[]): Promise<number[][]> {
  const BATCH = 50;
  const all: number[][] = [];
  for (let i = 0; i < texts.length; i += BATCH) {
    const res = await openai.embeddings.create({
      model: "text-embedding-3-small",
      input: texts.slice(i, i + BATCH),
      dimensions: 1536,
    });
    all.push(...res.data.sort((a, b) => a.index - b.index).map((d) => d.embedding));
    if (i + BATCH < texts.length) await new Promise((r) => setTimeout(r, 500));
  }
  return all;
}

// ─── 1文書を投入 ─────────────────────────────────────────────────────────────

async function seedDocument(doc: {
  title: string;
  source_type: string;
  source_url?: string;
  description?: string;
  content: string;
}) {
  // 既存チェック（Prisma経由）
  const existing = await prisma.knowledgeDocument.findFirst({ where: { title: doc.title } });
  if (existing) {
    console.log(`  → スキップ（既登録）`);
    return;
  }

  const chunks = splitIntoChunks(doc.content);
  console.log(`  チャンク数: ${chunks.length} — Embedding生成中...`);

  const embeddings = await embedTexts(chunks);

  // knowledge_documents を INSERT（Prisma）
  const created = await prisma.knowledgeDocument.create({
    data: {
      title: doc.title,
      source_type: doc.source_type,
      source_url: doc.source_url ?? null,
      description: doc.description ?? null,
    },
  });

  // knowledge_chunks は embedding(vector型)のため executeRawUnsafe でバルク挿入
  for (let idx = 0; idx < chunks.length; idx++) {
    await prisma.$executeRawUnsafe(
      `INSERT INTO knowledge_chunks (document_id, chunk_index, content, embedding)
       VALUES ($1::uuid, $2, $3, $4::vector)`,
      created.id,
      idx,
      chunks[idx],
      `[${embeddings[idx].join(",")}]`
    );
  }

  console.log(`  ✓ 完了: ${doc.title} (${chunks.length} チャンク)`);
}

